import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import tensorflow as tf

from sklearn.preprocessing import MinMaxScaler
from sklearn.metrics import mean_absolute_error, mean_squared_error
from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Bidirectional
from tensorflow.keras.layers import MultiHeadAttention, LayerNormalization

# Fix randomness
np.random.seed(42)
tf.random.set_seed(42)

# Load data
data = pd.read_csv("network_traffic.csv")
data['time'] = pd.to_datetime(data['time'])
data = data.sort_values('time')

traffic = data['bytes_sent'].values.reshape(-1,1)

# Scaling
scaler = MinMaxScaler()
traffic_scaled = scaler.fit_transform(traffic)

# Create sequences
def create_dataset(data, time_step=10):
    X, y = [], []
    for i in range(len(data)-time_step-1):
        X.append(data[i:(i+time_step), 0])
        y.append(data[i + time_step, 0])
    return np.array(X), np.array(y)

X, y = create_dataset(traffic_scaled, 10)
X = X.reshape(X.shape[0], X.shape[1], 1)

# Split
train_size = int(len(X)*0.8)
X_train, X_test = X[:train_size], X[train_size:]
y_train, y_test = y[:train_size], y[train_size:]

# ---------------- MODELS ---------------- #

# LSTM
model_lstm = Sequential([LSTM(50), Dense(1)])
model_lstm.compile(optimizer='adam', loss='mse')
model_lstm.fit(X_train, y_train, epochs=10, batch_size=32, verbose=0)

# BiLSTM
model_bilstm = Sequential([Bidirectional(LSTM(50)), Dense(1)])
model_bilstm.compile(optimizer='adam', loss='mse')
model_bilstm.fit(X_train, y_train, epochs=10, batch_size=32, verbose=0)

# Transformer
def transformer_model():
    inputs = tf.keras.Input(shape=(X_train.shape[1], 1))
    att = MultiHeadAttention(num_heads=2, key_dim=2)(inputs, inputs)
    x = LayerNormalization()(att)
    x = tf.keras.layers.Flatten()(x)
    outputs = Dense(1)(x)
    model = tf.keras.Model(inputs, outputs)
    model.compile(optimizer='adam', loss='mse')
    return model

model_transformer = transformer_model()
model_transformer.fit(X_train, y_train, epochs=10, batch_size=32, verbose=0)

# Self-Attention
def attention_model():
    inputs = tf.keras.Input(shape=(X_train.shape[1], 1))
    att = tf.keras.layers.Attention()([inputs, inputs])
    x = tf.keras.layers.Flatten()(att)
    outputs = Dense(1)(x)
    model = tf.keras.Model(inputs, outputs)
    model.compile(optimizer='adam', loss='mse')
    return model

model_attention = attention_model()
model_attention.fit(X_train, y_train, epochs=10, batch_size=32, verbose=0)

# ---------------- EVALUATION ---------------- #

def evaluate(model):
    pred = model.predict(X_test, verbose=0)
    pred = scaler.inverse_transform(pred)
    actual = scaler.inverse_transform(y_test.reshape(-1,1))

    mae = mean_absolute_error(actual, pred)
    rmse = np.sqrt(mean_squared_error(actual, pred))
    mape = np.mean(np.abs((actual - pred)/actual))*100
    return mae, rmse, mape

results = {
    'LSTM': evaluate(model_lstm),
    'BiLSTM': evaluate(model_bilstm),
    'Transformer': evaluate(model_transformer),
    'SelfAttention': evaluate(model_attention)
}

results_df = pd.DataFrame(results).T
results_df.columns = ['MAE', 'RMSE', 'MAPE']

print(results_df)

best_model = results_df['RMSE'].idxmin()
print("Best Model:", best_model)

import matplotlib.pyplot as plt
import numpy as np

models = results_df.index

mae = results_df['MAE']
rmse = results_df['RMSE']
mape = results_df['MAPE']

# Convert RMSE to score (lower error = better)
scores = 1 / rmse
scores = scores / scores.sum()

best_model = results_df['RMSE'].idxmin()
explode = [0.1 if m == best_model else 0 for m in models]

# Create 2x2 layout
fig, axs = plt.subplots(2, 2, figsize=(12, 10))

# 🔹 Graph 1: MAE
axs[0, 0].bar(models, mae)
axs[0, 0].set_title("MAE Comparison")
